  const cfg = window.siteConfig;
  const LS_KEY = "hnctech_theme_overrides";
  const CONTENT_KEY = "hnctech_content";
  const $  = (sel) => document.querySelector(sel);
  const $$ = (sel) => Array.from(document.querySelectorAll(sel));

  /* ---------------------------------------------------------- COLOR HELPERS */
  function clamp(n) { return Math.max(0, Math.min(255, Math.round(n))); }
  function hexToRgb(hex) {
    const h = hex.replace("#", "");
    const v = h.length === 3 ? h.split("").map((c) => c + c).join("") : h;
    return { r: parseInt(v.slice(0, 2), 16), g: parseInt(v.slice(2, 4), 16), b: parseInt(v.slice(4, 6), 16) };
  }
  function rgbToHex(r, g, b) {
    return "#" + [r, g, b].map((x) => clamp(x).toString(16).padStart(2, "0")).join("");
  }
  function mixBlack(hex, amt) { const c = hexToRgb(hex); const k = 1 - amt; return rgbToHex(c.r * k, c.g * k, c.b * k); }
  function mixWhite(hex, amt) { const c = hexToRgb(hex); return rgbToHex(c.r + (255 - c.r) * amt, c.g + (255 - c.g) * amt, c.b + (255 - c.b) * amt); }

  /* ------------------------------------------------------------- THEME/SEO */
  function loadSavedTheme() {
    try { return JSON.parse(localStorage.getItem(LS_KEY)) || {}; } catch (e) { return {}; }
  }
  function saveTheme(overrides) {
    try { localStorage.setItem(LS_KEY, JSON.stringify(overrides)); } catch (e) {}
  }

  /* ---------------------------------------- EDITABLE CONTENT (Option B) */
  function loadContent() {
    try { return JSON.parse(localStorage.getItem(CONTENT_KEY)) || {}; } catch (e) { return {}; }
  }
  function saveContent(obj) {
    try {
      if (obj === null) localStorage.removeItem(CONTENT_KEY);
      else localStorage.setItem(CONTENT_KEY, JSON.stringify(obj));
    } catch (e) {}
  }
  // Overlay any browser-saved edits on top of the defaults in site-config.js
  function applyContentOverrides() {
    const saved = loadContent();
    if (Array.isArray(saved.services))     cfg.services.items     = saved.services;
    if (Array.isArray(saved.projects))     cfg.projects.items     = saved.projects;
    if (Array.isArray(saved.testimonials)) cfg.testimonials.items = saved.testimonials;
  }

  function loadFonts(display, body) {
    const fam = (name) => name.trim().replace(/\s+/g, "+") + ":wght@400;500;600;700";
    const url = "https://fonts.googleapis.com/css2?family=" + fam(display) + "&family=" + fam(body) + "&display=swap";
    $("#font-link").setAttribute("href", url);
  }

  function applyTheme(theme) {
    const root = document.documentElement.style;
    root.setProperty("--c-primary", theme.primaryColor);
    root.setProperty("--c-primary-deep", mixBlack(theme.primaryColor, 0.30));
    root.setProperty("--c-primary-soft", mixWhite(theme.primaryColor, 0.12));
    root.setProperty("--c-secondary", theme.secondaryColor);
    root.setProperty("--c-accent", theme.accentColor);
    root.setProperty("--c-bg", theme.bgColor);
    root.setProperty("--c-text", theme.textColor);
    root.setProperty("--font-display", `"${theme.fontDisplay}", system-ui, sans-serif`);
    root.setProperty("--font-body", `"${theme.fontBody}", system-ui, sans-serif`);
    loadFonts(theme.fontDisplay, theme.fontBody);
  }

  function applySeo(seo) {
    document.title = seo.title;
    $("#meta-description").setAttribute("content", seo.description);
    $("#meta-keywords").setAttribute("content", seo.keywords || "");
    $("#og-title").setAttribute("content", seo.title);
    $("#og-description").setAttribute("content", seo.description);
    if (seo.ogImage) $("#og-image").setAttribute("content", seo.ogImage);

    const ld = {
      "@context": "https://schema.org",
      "@type": "Organization",
      "name": cfg.brand.legalName || cfg.brand.companyName,
      "url": seo.url || "",
      "description": seo.description,
      "address": { "@type": "PostalAddress", "addressLocality": cfg.contact.address },
      "email": cfg.contact.email,
      "telephone": cfg.contact.phone
    };
    if (seo.ogImage) ld.logo = seo.ogImage;
    $("#ld-json").textContent = JSON.stringify(ld, null, 2);
  }

  // Set the browser-tab icon (favicon) and the iOS home-screen icon from config.
  function applyFavicon(brand) {
    if (!brand || !brand.favicon) return;
    const ensure = (rel) => {
      let link = document.querySelector(`link[rel="${rel}"]`);
      if (!link) { link = document.createElement("link"); link.setAttribute("rel", rel); document.head.appendChild(link); }
      return link;
    };
    const icon = ensure("icon");
    icon.setAttribute("href", brand.favicon);
    if (/\.png$/i.test(brand.favicon)) icon.setAttribute("type", "image/png");
    else if (/\.svg$/i.test(brand.favicon)) icon.setAttribute("type", "image/svg+xml");
    else if (/\.ico$/i.test(brand.favicon)) icon.setAttribute("type", "image/x-icon");
    ensure("apple-touch-icon").setAttribute("href", brand.favicon);
  }

  /* --------------------------------------------------------- SMALL BUILDERS */
  function brandMarkup() {
    const b = cfg.brand;
    // If a full logo image is provided, use it as the ENTIRE brand block.
    // (The PNG typically already contains the company name, so we don't repeat it.)
    if (b.logoImage) {
      return `<img class="brand-img" src="${b.logoImage}" alt="${b.companyName || b.legalName || "Logo"}">`;
    }
    // Fallback: stylised "mark + text" lockup using initial + tagline.
    const mark = `<span class="mark">${b.logoText || b.companyName.charAt(0)}</span>`;
    const name = b.companyName.split(" ")[0] || b.companyName;
    return `${mark}<span>${name}<span class="tagline">${b.tagline || ""}</span></span>`;
  }

