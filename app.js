  let testiTimer = null;
  function initTestimonials() {
    const tTrack = $("#testiTrack");
    if (!tTrack) return;
    if (testiTimer) { clearInterval(testiTimer); testiTimer = null; }
    const slides = tTrack.children.length;
    const tNav = $("#testiNav");
    let tIndex = 0;
    for (let i = 0; i < slides; i++) {
      const dot = document.createElement("button");
      dot.className = "testi-dot" + (i === 0 ? " active" : "");
      dot.setAttribute("aria-label", "Go to testimonial " + (i + 1));
      dot.addEventListener("click", () => { goTo(i); restart(); });
      tNav.appendChild(dot);
    }
    const dots = Array.from(tNav.children);
    function goTo(i) {
      tIndex = i;
      tTrack.style.transform = `translateX(-${i * 100}%)`;
      dots.forEach((d, di) => d.classList.toggle("active", di === i));
    }
    function restart() { if (testiTimer) clearInterval(testiTimer); testiTimer = setInterval(() => goTo((tIndex + 1) % slides), 6000); }
    if (slides > 1) restart();
  }

  function initNetworkCanvas() {
    const canvas = $("#network-canvas");
    if (!canvas || canvas.style.display === "none") return;
    const ctx = canvas.getContext("2d");
    const reduce = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
    const DPR = Math.min(window.devicePixelRatio || 1, 2);
    let w, h, nodes, raf;

    // Pull accent color from the live theme so the animation always matches.
    const accent = getComputedStyle(document.documentElement).getPropertyValue("--c-accent").trim() || "#00D4FF";
    const rgb = hexToRgb(accent);
    const lineRGB = `${rgb.r},${rgb.g},${rgb.b}`;
    const dotColor = rgbToHex(Math.min(255, rgb.r + 80), Math.min(255, rgb.g + 30), 255);

    function resize() {
      w = canvas.clientWidth; h = canvas.clientHeight;
      canvas.width = w * DPR; canvas.height = h * DPR;
      ctx.setTransform(DPR, 0, 0, DPR, 0, 0);
      const count = Math.min(90, Math.floor(w / 16));
      nodes = Array.from({ length: count }, () => ({
        x: Math.random() * w, y: Math.random() * h,
        vx: (Math.random() - 0.5) * 0.35, vy: (Math.random() - 0.5) * 0.35
      }));
    }
    function draw() {
      ctx.clearRect(0, 0, w, h);
      for (const n of nodes) {
        n.x += n.vx; n.y += n.vy;
        if (n.x < 0 || n.x > w) n.vx *= -1;
        if (n.y < 0 || n.y > h) n.vy *= -1;
      }
      for (let i = 0; i < nodes.length; i++) {
        for (let j = i + 1; j < nodes.length; j++) {
          const a = nodes[i], b = nodes[j];
          const dist = Math.hypot(a.x - b.x, a.y - b.y);
          if (dist < 130) {
            ctx.strokeStyle = `rgba(${lineRGB},${(1 - dist / 130) * 0.5})`;
            ctx.lineWidth = 0.7;
            ctx.beginPath(); ctx.moveTo(a.x, a.y); ctx.lineTo(b.x, b.y); ctx.stroke();
          }
        }
      }
      for (const n of nodes) {
        ctx.beginPath(); ctx.arc(n.x, n.y, 1.8, 0, Math.PI * 2);
        ctx.fillStyle = dotColor; ctx.fill();
      }
      raf = requestAnimationFrame(draw);
    }
    function start() {
      resize();
      if (!reduce) draw();
      else { ctx.clearRect(0, 0, w, h); for (const n of nodes) { ctx.beginPath(); ctx.arc(n.x, n.y, 1.8, 0, Math.PI * 2); ctx.fillStyle = dotColor; ctx.fill(); } }
    }
    window.addEventListener("resize", () => { cancelAnimationFrame(raf); start(); });
    start();
  }

